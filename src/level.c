bool onGeom(int tile) {
	return tile >= 1 && tile <= 2;
}

//40x30
bool checkPlat(Coord c) {
	return onGeom(map[(int)(c.y / 8)][(int)(c.x/8)]);
}

bool checkWall(Coord c) {
	return onGeom(map[(int)(c.y / 8)][(int)(c.x / 8)]);
}

void loadLevel() {
	for (int i = 0; i < 30; i++) {
		for (int j = 0; j < 40; j++) {
			switch (map[i][j]) {
			case 9:
				spawnChar(makeCoord(j*TILE_SIZE, i*TILE_SIZE));
				break;
			}
		}
	}
}

void drawLevel() {
//	drawSimpleTile("bg2.png", makeCoord(0, 0));

	for (int i = 0; i < 30; i++) {
		for (int j = 0; j < 40; j++) {
			switch(map[i][j]) {
				case 1:
					drawSimpleTile("tile-01b.png", makeCoord(j*TILE_SIZE, i*TILE_SIZE));
					break;
				case 2:
					drawSimpleTile("tile-00b.png", makeCoord(j*TILE_SIZE, i*TILE_SIZE));
					break;
				case 3:
					drawSimpleTile("tile-00c.png", makeCoord(j*TILE_SIZE, i*TILE_SIZE));
					break;
				case 9:
					spawnChar(makeCoord(j*TILE_SIZE, i*TILE_SIZE));
					break;
			}
		}
	}
}
